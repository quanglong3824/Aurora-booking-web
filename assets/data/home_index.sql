  `room_premium_amenity_bath` VARCHAR(150),
  `room_premium_amenity_service` VARCHAR(150),
  `services_title` VARCHAR(150),
  `services_desc` VARCHAR(255),
  `service_items` TEXT, -- JSON array of {title, desc}
  `about_title` VARCHAR(255),
  `about_text` TEXT,
  `gallery_title` VARCHAR(150),
  `gallery_subtitle` VARCHAR(255),
