  `hero_images` TEXT, -- JSON array
  `hero_slide_interval` INT UNSIGNED,
  `booking_title` VARCHAR(150),
  `booking_subtitle` VARCHAR(255),
  `rooms_title` VARCHAR(150),
  `rooms_desc` VARCHAR(255),
